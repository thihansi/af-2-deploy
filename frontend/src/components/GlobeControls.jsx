// Helper functions for dynamic styling
function getGradient(mode) {
  switch (mode) {
    case "realistic":
      return "from-blue-600 to-indigo-600";
    case "night":
      return "from-indigo-700 to-violet-700";
    case "topo":
      return "from-emerald-600 to-teal-600";
    case "cartoon":
      return "from-amber-500 to-orange-600";
    default:
      return "from-blue-600 to-indigo-600";
  }
}

function getShadowColor(mode) {
  switch (mode) {
    case "realistic":
      return "blue";
    case "night":
      return "indigo";
    case "topo":
      return "emerald";
    case "cartoon":
      return "amber";
    default:
      return "blue";
  }
}

